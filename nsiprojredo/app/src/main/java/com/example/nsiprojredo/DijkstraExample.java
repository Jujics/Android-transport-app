    static final int V = 60;

    int minDistance(int dist[], Boolean sptSet[]) {
        int min = Integer.MAX_VALUE, min_index = -1;

        for (int v = 0; v < V; v++)
            if (!sptSet[v] && dist[v] <= min) {
                min = dist[v];
                min_index = v;
            }
        return min_index;
    }

    int printSolution(int[] dist, int n , int fin, int deb, String d , String e,TextView textvw,TextView textvw2) {
        for (int i = 0; i < V; i++)
            if (i == fin) {
                textvw.setText(dist[i]+" Sec");
                textvw2.setText("De "+e+" a "+d);

            }
        return n;
    }

    void dijkstra(int graph[][], int src, int fin, String d ,String e, TextView textvw,TextView textvw2) {
        int dist[] = new int[V];

        Boolean sptSet[] = new Boolean[V];

        for (int i = 0; i < V; i++) {
            dist[i] = Integer.MAX_VALUE;
            sptSet[i] = false;
        }

        dist[src] = 0;

        for (int count = 0; count < V - 1; count++) {
            int u = minDistance(dist, sptSet);

            sptSet[u] = true;

            for (int v = 0; v < V; v++)

                if (!sptSet[v] && graph[u][v] != 0 && dist[u] != Integer.MAX_VALUE && dist[u] + graph[u][v] < dist[v])
                    dist[v] = dist[u] + graph[u][v];
        }
        printSolution(dist, V, fin, src ,d ,e, textvw, textvw2);

    }




